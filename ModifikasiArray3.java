public class ModifikasiArray3 {
    public static void main(String[] args) {
        int[] bil = {5, 13, -7, 17};

        bil[0] = 5;
        bil[1] = 13;
        bil[2] = -7;
        bil[3] = 17;

        for (int i = 0; i < bil.length; i++) {
            System.out.println(bil[i]);
        }
    }
}
